const hr = '\n\n---\n\n'

function section(title: string, lines: string[]) {
  return [`## ${title}`, ...lines].join('\n')
}

function bullets(items: string[]) {
  return items.map((x) => `- ${x}`).join('\n')
}

function examQs(items: string[]) {
  return items.map((x, i) => `${i + 1}) ${x}`).join('\n')
}

function miniGlossary(items: Array<{ term: string; def: string }>) {
  return items.map((x) => `- **${x.term}** — ${x.def}`).join('\n')
}

function para(lines: string[]) {
  return lines.join('\n')
}

function mcq(items: Array<{ q: string; options: string[]; correct: number; explain: string }>) {
  return items
    .map((x, i) => {
      const opts = x.options.map((o, j) => `- ${String.fromCharCode(65 + j)}) ${o}`).join('\n')
      const ans = String.fromCharCode(65 + x.correct)
      return [`**Тест ${i + 1}:** ${x.q}`, opts, `**Ҷавоб:** ${ans}`, `**Шарҳ:** ${x.explain}`].join('\n\n')
    })
    .join('\n\n')
}

function checklist(items: string[]) {
  return items.map((x) => `- [ ] ${x}`).join('\n')
}

